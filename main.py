def aiol(s):
    res=[]
    for i in s:
        res.append(alpha_dict[i])
    for i in range(1,len(s)):
        m=max(res[0][0])+2
        res[0]=[j+[x+m for x in y] for y,j in zip(res[i],res[0])]
    return res[0]
def alpha_pattern(s, sl='n'):
    """
    pattern function
    :param s:
    :param sl:
    """
    if sl == 'n':
        for ch in s:
            for i in alpha_dict[ch]:
                if isinstance(i, list):
                    for j in range(max(i)):
                        if j+1 in i:
                            print('*', end=' ')
                        else:
                            print(' ', end=' ')
                    print()
                else:
                    for k in range(i):
                        print('*', end=' ')
                    print()
    else:
        for i in aiol(s):
            if isinstance(i, list):
                for j in range(0, max(i)):
                    if j+1 in i:
                        print('*', end=' ')
                    else:
                        print(' ', end=' ')
